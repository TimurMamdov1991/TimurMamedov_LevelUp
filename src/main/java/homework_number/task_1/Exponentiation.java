package homework_number.task_1;

public class Exponentiation {

  public int calcExponentiation(int NumOne) {
    return NumOne * NumOne;
  }

  public int calcExponentiationTwo(int NumTwo) {
    return NumTwo * NumTwo;
  }

  public double calcExponentiation(double NumOne) {
    return NumOne * NumOne;
  }

  public double calcExponentiationTwo(double NumTwo) {
    return NumTwo * NumTwo;
  }
}
